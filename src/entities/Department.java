package entities;

public class Department {
	
	private String name;

	
	// constructor
	public Department(String name) {
		this.name = name;
	}
	
	// empty constructor
	public Department() {
		
	}
	
	
	// Getters - Setters
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
